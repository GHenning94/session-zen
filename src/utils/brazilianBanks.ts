// Helper function to format bank option display
export const formatBankOption = (bank: typeof BRAZILIAN_BANKS[0]) => {
  return `${bank.code} - ${bank.name}`;
};

// Format agency with max 5 digits
export const formatAgencia = (value: string): string => {
  const cleaned = value.replace(/\D/g, '');
  return cleaned.slice(0, 5);
};

// Format account number with digit (XXXXX-X format, up to 13 digits + 1 check digit)
export const formatConta = (value: string): string => {
  const cleaned = value.replace(/\D/g, '');
  if (cleaned.length <= 1) return cleaned;
  
  // Max 13 digits + 1 check digit
  const limited = cleaned.slice(0, 14);
  const main = limited.slice(0, -1);
  const digit = limited.slice(-1);
  
  return `${main}-${digit}`;
};
